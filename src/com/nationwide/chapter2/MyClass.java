package com.nationwide.chapter2;

public class MyClass {
	private String cartContents;
	private int itemTotal;
	private float totalPrice;
	/**
	 * @return the cartContents
	 */
	public String getCartContents() {
		return cartContents;
	}
	/**
	 * @param cartContents the cartContents to set
	 */
	public void setCartContents(String cartContents) {
		this.cartContents = cartContents;
	}
	/**
	 * @return the itemTotal
	 */
	public int getItemTotal() {
		return itemTotal;
	}
	/**
	 * @param itemTotal the itemTotal to set
	 */
	public void setItemTotal(int itemTotal) {
		this.itemTotal = itemTotal;
	}
	/**
	 * @return the totalPrice
	 */
	public float getTotalPrice() {
		return totalPrice;
	}
	/**
	 * @param totalPrice the totalPrice to set
	 */
	public void setTotalPrice(float totalPrice) {
		this.totalPrice = totalPrice;
	}

}
