package com.nationwide.chapter5;

public class Assigment2 {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		// Example 1: Casting long to int
		long y = 42;
		int x = (int)y;
		System.out.println(x);

		// Example 2: Casting int to short
		int i = 34;
		short s = (short)i;
		System.out.println(s);

		// Example 3: Casting long to byte
		long l = 100;
		byte b = (byte)l;
		System.out.println(b);

		// Example 4: Casting float to short
		float f = 42.22f;
		short s2 = (short) f;
		System.out.println(s2);

	}

}
